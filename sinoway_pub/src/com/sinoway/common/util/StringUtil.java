package com.sinoway.common.util;

import java.security.MessageDigest;

/**
 * 通用字符处理工具类
 * @author LiuZhen
 * @version 1.0
 * 2015-10-30
 */
public class StringUtil {

	
	/**
	 * null转换空
	 * @param str
	 * @return
	 */
	public static String NullToString(String str){
		
		if(str == null){
			return "";
		}
		return str;
	}
	
	/**
	 * 去前后空格
	 * @param str
	 * @return
	 */
	public static String trim(String str) {
		if(str == null){
			return "";
		}
		
		return str.trim();
		
	}

}
