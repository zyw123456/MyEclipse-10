package com.sinoway.acc.entity;

public class PeopleShowInfo {
	private String sid;
	private String peopleState;
	private String peopleName;
	private String organizeInfo;
	private String peopleCode;
	private String pwd;
	private String usrId;
	private String orgNo;
	private String orgName;
	
	

	public String getOrgNo() {
		return orgNo;
	}
	public void setOrgNo(String orgNo) {
		this.orgNo = orgNo;
	}
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getUsrId() {
		return usrId;
	}
	public void setUsrId(String usrId) {
		this.usrId = usrId;
	}
	public String getPeopleCode() {
		return peopleCode;
	}
	public void setPeopleCode(String peopleCode) {
		this.peopleCode = peopleCode;
	}
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getPeopleState() {
		return peopleState;
	}
	public void setPeopleState(String peopleState) {
		this.peopleState = peopleState;
	}
	public String getPeopleName() {
		return peopleName;
	}
	public void setPeopleName(String peopleName) {
		this.peopleName = peopleName;
	}
	public String getOrganizeInfo() {
		return organizeInfo;
	}
	public void setOrganizeInfo(String organizeInfo) {
		this.organizeInfo = organizeInfo;
	}
	
	public PeopleShowInfo() {
		super();
	}
	
	public PeopleShowInfo(String sid, String peopleState, String peopleName,
			String organizeInfo) {
		super();
		this.sid = sid;
		this.peopleState = peopleState;
		this.peopleName = peopleName;
		this.organizeInfo = organizeInfo;
	}
	
	public PeopleShowInfo(String sid, String peopleState, String peopleName,
			String organizeInfo, String peopleCode, String pwd, String usrId) {
		super();
		this.sid = sid;
		this.peopleState = peopleState;
		this.peopleName = peopleName;
		this.organizeInfo = organizeInfo;
		this.peopleCode = peopleCode;
		this.pwd = pwd;
		this.usrId = usrId;
	}
	public PeopleShowInfo(String usrId, String peopleName, String peopleCode) {
		super();
		this.peopleName = peopleName;
		this.peopleCode = peopleCode;
		this.usrId = usrId;
	}
	
}
